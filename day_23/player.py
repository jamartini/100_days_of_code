from turtle import Turtle

HEIGHT = 600
WIDTH = 16 * HEIGHT / 9
STARTING_POSITION = (0, -((HEIGHT/2)-20))
MOVE_DISTANCE = 10
FINISH_LINE_Y = 280


class Player(Turtle):
    def __init__(self):
        super().__init__()
        self.shape('turtle')
        self.pu()
        self.go_to_start()
        self.setheading(90)

    def go_up(self):
        self.forward(MOVE_DISTANCE)

    def go_to_start(self):
        self.goto(STARTING_POSITION)
